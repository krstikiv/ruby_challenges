weather = 'sunny'

case weather
when 'sunny' 
	puts "It's hot!"
when 'cloudy' 
	puts "Lots of clouds in the sky."
when 'foggy' 
	puts "Seems like a murder scene from a scary movie with all this fog."
when 'rainy' 
	puts "Rain rain raindrops on my head."
when 'snowy' 
	puts 'Finally some snow!'
else 
	puts "A weather never experienced before!"
end

